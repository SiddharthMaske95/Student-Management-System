package Model;
public class Student
{
	String id;
	String name;
	String dob;
	String doj;
	public String getid() {
		return id;
	}
	public void setid(String id) {
		this.id = id;
	}
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name = name;
	}
	
	public String getdob() {
		return dob;
	}
	public void setdob(String dob) {
		this.dob = dob;
	}
	public String getdoj() {
		return doj;
	}
	public void setdoj(String doj) {
		this.doj = doj;
	}
	
	  
}
